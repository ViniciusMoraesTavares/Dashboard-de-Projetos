let operador = "/"
let a = 10
let b = 0

if(operador === "+"){
    console.log("Soma =", a + b)
}
else if(operador === "-"){
    console.log("Subtrair =", a - b)
}
else if(operador === "*"){
    console.log("Multiplicação =", a * b)
}
else if(operador === "/"){
    console.log("Divisão = ", a / b)
}
else{
    console.log("Operador não encontrado")
}